/*
 * tflite.c — Tiny int8 1-D CNN inference engine for stroke classification
 * Author: jayis1
 * Copyright (c) 2026 jayis1 — MIT License
 *
 * Network topology (int8 quantized, CMSIS-NN-compatible shapes):
 *
 *   input   : 64 × 3   int8
 *   conv1   : Conv1D(8,  k=5, stride=1, ReLU)   out: 64 × 8
 *   conv2   : Conv1D(16, k=5, stride=2, ReLU)  out: 32 × 16
 *   conv3   : Conv1D(32, k=3, stride=2, ReLU)  out: 16 × 32
 *   conv4   : Conv1D(64, k=3, stride=1, ReLU)  out: 16 × 64
 *   pool    : average over time                    out: 64
 *   fc      : Linear(64 → 56)                       out: 56 logits
 *
 * Weight layout in the blob (big chunks; see `glyphflow_weights_default`):
 *   conv1_w[8*3*5]   conv1_b[8]
 *   conv2_w[16*8*5]  conv2_b[16]
 *   conv3_w[32*16*3] conv3_b[32]
 *   conv4_w[64*32*3] conv4_b[64]
 *   fc_w[56*64]      fc_b[56]
 * Total int8 weights: 120 + 640 + 1536 + 6144 + 3584 = 12024
 * Total int16/32 biases (stored as int32 scaled): 8+16+32+64+56 = 176 × 4 = 704 B
 *
 * For real deployments this blob is replaced via BLE OTA. Here we ship a
 * deterministic placeholder blob so the firmware has a self-contained
 * reference; the placeholder produces a valid but untrained output (the
 * classification layer is exercised; accuracy is random until trained).
 */
